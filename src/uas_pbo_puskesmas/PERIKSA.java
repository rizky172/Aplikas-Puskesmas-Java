    private void btnhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnhapusActionPerformed
        int ok = JOptionPane.showConfirmDialog(null, "Apakah anda yakin ingin menghapus data",
            "Konfirmasi Dialog", JOptionPane.YES_NO_OPTION);
    if(ok==0){
        String sql = "delete from periksa where id_periksa='"+txtkode.getText()+"'";
        try {
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data Berhasil Dihapus");
            kosong();
            txtkode.requestFocus();
            dataTabel();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Data Gagal Dihapus "+e);
        }
        }
    }//GEN-LAST:event_btnhapusActionPerformed

    private void btntambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btntambahActionPerformed
        String tombol = btntambah.getText();
        if (tombol.equals("TAMBAH")){
            aktif();
            kosong();
            btntambah.setText("SIMPAN");
            btnedit.setEnabled(false);
            btnhapus.setEnabled(false);
            autoNumber();
        }else{
            String sql = "insert into periksa values(?,?,?,?,?,?,?)";
            try {
                PreparedStatement stat = conn.prepareStatement(sql);
                stat.setString(1, txtkode.getText());
                stat.setString(2, txtkddokter.getText());
                stat.setString(3, txtkdpasien.getText());
                stat.setString(4, txtberat.getText());
                stat.setString(5, txtdarah.getText());
                stat.setString(6, txtalergi.getSelectedItem().toString());
                stat.setString(7, txtkeluhan.getText());
                stat.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan");
                kosong();
                dataTabel();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Data Gagal Disimpan "+e);
                kosong();
            }
            btntambah.setText("TAMBAH");
            non_aktif();
        }
    }//GEN-LAST:event_btntambahActionPerformed

