    private void dataTabel(){
    Object[] Baris ={"Kode","Nama Dokter","Nama Pasien","Umur","Jenis Kelamin","Berat Badan","Tensi Darah","Alergi Obat","Keluhan"};
        tabmode = new DefaultTableModel(null, Baris);
        tabel.setModel(tabmode);
        String sql = "SELECT * FROM periksa, pasien, dokter WHERE pasien.id_pasien = periksa.id_pasien JOIN dokter.id_dokter = periksa.id_dokter";
        try {
            Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while(hasil.next()){
                String kode = hasil.getString("id_periksa");
                String nama = hasil.getString("nama_dokter");
                String nm = hasil.getString("nama_pasien");
                String umur = hasil.getString("umur_pasien");
                String jenis = hasil.getString("jenis_kelamin_pasien");
                String bb = hasil.getString("berat_badan");
                String darah = hasil.getString("tensi_darah");
                String alergi = hasil.getString("alergi_obat");
                String keluhan = hasil.getString("keluhan");
                String[] data={kode, nama, nm, umur, jenis, bb, darah, alergi, keluhan};
                tabmode.addRow(data);
            }
        } catch (Exception e) {
        }       
    }
    
    
