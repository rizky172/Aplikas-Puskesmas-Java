    private void txtnamapasienActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtnamapasienActionPerformed
        String sql = "SELECT pasien.id_pasien, pasien.umur_pasien, periksa.id_periksa, periksa.keluhan  FROM pasien INNER JOIN periksa ON pasien.id_pasien = periksa.id_pasien WHERE nama_pasien='"+txtnamapasien.getSelectedItem()+"'";
        try {
            Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while(hasil.next()){
                txtkdpasien.setText(hasil.getString("id_pasien"));
                txtumur.setText(hasil.getString("umur_pasien"));
                txtkdperiksa.setText(hasil.getString("id_periksa"));
                txtkeluhan.setText(hasil.getString("keluhan"));
 
            }
            hasil.close();
            stat.close();
            } catch (SQLException ex) {
        }
    }//GEN-LAST:event_txtnamapasienActionPerformed

    private void btntambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btntambahActionPerformed
        String tombol = btntambah.getText();
        if (tombol.equals("TAMBAH")){
            aktif();
            kosong();
            btntambah.setText("SIMPAN");
            btnedit.setEnabled(false);
            btnhapus.setEnabled(false);
            autoNumber();
        }else{
            String tampilan ="yyyy-MM-dd" ; 
            SimpleDateFormat fm = new SimpleDateFormat(tampilan); 
            String tanggal = String.valueOf(fm.format(txttanggal.getDate()));
            String sql = "insert into resep values(?,?,?,?,?,?)";
            try {
                PreparedStatement stat = conn.prepareStatement(sql);
                stat.setString(1, txtkode.getText());
                stat.setString(2, txtkdpasien.getText());
                stat.setString(3, txtkdperiksa.getText());
                stat.setString(4, tanggal);
                stat.setString(5, txtobat.getText());
                stat.setString(6, txtpemakaian.getText());
                stat.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan");
                kosong();
                dataTabel();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Data Gagal Disimpan "+e);
                kosong();
            }
            btntambah.setText("TAMBAH");
            non_aktif();
        }
    }//GEN-LAST:event_btntambahActionPerformed

