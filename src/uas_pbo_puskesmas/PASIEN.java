    private void dataTabel(){
    Object[] Baris ={"Kode","Nama Petugas","Nama Pasien","Umur","Jenis Kelamin","Tgl Lahir","Telepon","Alamat","Hari/Periksa","Tgl/Periksa"};
        tabmode = new DefaultTableModel(null, Baris);
        tabel.setModel(tabmode);
        String sql = "SELECT * FROM pasien, petugas WHERE petugas.id_petugas = pasien.id_petugas";
        try {
            Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while(hasil.next()){
                String kode = hasil.getString("id_pasien");
                String nama = hasil.getString("nama_petugas");
                String nm = hasil.getString("nama_pasien");
                String umur = hasil.getString("umur_pasien");
                String jenis = hasil.getString("jenis_kelamin_pasien");
                String tgl = hasil.getString("tgl_lahir_pasien");
                String tlp = hasil.getString("telepon_pasien");
                String alamat = hasil.getString("alamat_pasien");
                String hperiksa = hasil.getString("jadwal_petugas");
                String tperiksa = hasil.getString("tgl_jadwal_petugas");
                String[] data={kode, nama, nm, umur, jenis, tgl, tlp, alamat, hperiksa, tperiksa};
                tabmode.addRow(data);
            }
        } catch (Exception e) {
        }       
    }
    
